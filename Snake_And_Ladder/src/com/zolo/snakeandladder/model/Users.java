package com.zolo.snakeandladder.model;

/**
 * The Class Users.
 */
public class Users{
	
	/** The user name. */
	private String userName;
	

	/** The curr position. */
	private int currPosition;
	
	/** The prev position. */
	private int prevPosition;
	
	/**
	 * Gets the user name.
	 *
	 * @return the user name
	 */
	public String getUserName() {
		return userName;
	}
	
	/**
	 * Sets the user name.
	 *
	 * @param userName the new user name
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	
	/**
	 * Gets the curr position.
	 *
	 * @return the curr position
	 */
	public int getCurrPosition() {
		return currPosition;
	}
	
	/**
	 * Sets the curr position.
	 *
	 * @param currPosition the new curr position
	 */
	public void setCurrPosition(int currPosition) {
		this.currPosition = currPosition;
	}
	
	/**
	 * Gets the prev position.
	 *
	 * @return the prev position
	 */
	public int getPrevPosition() {
		return prevPosition;
	}
	
	/**
	 * Sets the prev position.
	 *
	 * @param prevPosition the new prev position
	 */
	public void setPrevPosition(int prevPosition) {
		this.prevPosition = prevPosition;
	} 
	
	
}
